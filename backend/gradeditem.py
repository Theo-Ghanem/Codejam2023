class GradedItem :

    def __init__(self, id, name, type, dueDate, weight, grade, file, assignees, course, timelineItems):
        self.id = id
        self.name = name
        self.type = type
        self.dueDate = dueDate
        self.weight = weight
        self.grade = grade
        self.file = file
        self.assignees = assignees
        self.course = course
        self.timelineItems = timelineItems

